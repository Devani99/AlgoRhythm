# Create database configuration
db_config = """import mongoose from 'mongoose';
import dotenv from 'dotenv';

dotenv.config();

const connectDB = async () => {
  try {
    const mongoURI = process.env.MONGODB_URI || 'mongodb://localhost:27017/travel-assistant';
    
    const conn = await mongoose.connect(mongoURI, {
      useNewUrlParser: true,
      useUnifiedTopology: true,
    });

    console.log(`âœ… MongoDB Connected: ${conn.connection.host}`);
    
    // Handle connection events
    mongoose.connection.on('error', (err) => {
      console.error('âŒ MongoDB connection error:', err);
    });

    mongoose.connection.on('disconnected', () => {
      console.log('âš ï¸ MongoDB disconnected');
    });

    mongoose.connection.on('reconnected', () => {
      console.log('ğŸ”„ MongoDB reconnected');
    });

  } catch (error) {
    console.error('âŒ Error connecting to MongoDB:', error);
    process.exit(1);
  }
};

export default connectDB;
"""

with open("travel-backend/config/database.js", "w") as f:
    f.write(db_config)

print("database.js configuration created successfully!")